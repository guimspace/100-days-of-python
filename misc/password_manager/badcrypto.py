class BadCrypto:
    def randbytes(num):
        return bytes(choices(POOL, k=num))

    def encrypt(password, data):
        salt = BadCrypto.randbytes(16)
        kdf = PBKDF2HMAC(
            algorithm=hashes.SHA256(),
            length=32,
            salt=salt,
            iterations=1000,
            backend=default_backend()
        )

        key = kdf.derive(bytes(password, "utf-8"))

        iv = BadCrypto.randbytes(12)
        encryptor = Cipher(
            algorithms.AES(key),
            modes.CBC(iv),
            backend=default_backend()
        ).encryptor()
        encryptor.authenticate_additional_data(salt + iv)

        padder = padding.PKCS7(256).padder()

        pt = bytes(data, "utf-8")
        try:
            padder.update(pt)
        except AlreadyFinalized:
            pass
        else:
            pt += padder.finalize()

        ct = encryptor.update(pt)

        return b64encode(salt + iv + ct).decode("utf-8")
